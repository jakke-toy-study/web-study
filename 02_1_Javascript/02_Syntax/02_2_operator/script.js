// 산술 연산자
let a = 10, b = 3;
console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.333...

// 비교 연산자
console.log(a > b); // true
console.log(a === b); // false

// 논리 연산자
let isOnline = true, isAdmin = false;
console.log(isOnline && isAdmin); // false
console.log(isOnline || isAdmin); // true
console.log(!isAdmin); // true