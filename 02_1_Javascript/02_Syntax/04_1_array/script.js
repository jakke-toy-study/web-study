// 배열 생성
const fruits = ["Apple", "Banana", "Cherry"];

// 배열의 길이 확인
console.log(fruits.length); // 3

// 배열 요소 접근
console.log(fruits[0]); // "Apple" (첫 번째 요소)
console.log(fruits[1]); // "Banana"
console.log(fruits[2]); // "Cherry"

// 배열 요소 변경
fruits[1] = "Blueberry";
console.log(fruits); // ["Apple", "Blueberry", "Cherry"]